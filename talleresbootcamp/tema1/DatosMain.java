package Ejercicios.tema1;

public class DatosMain {

    public static void main(String[] args) {

       // 1. Datos de tipo Numerico
       // 1.1 Enteros
        byte variable1 = 5;
        System.out.println("Este es un dato de tipo byte con valor asignado : " + variable1);

        short variable2 = 10;
        System.out.println("Este es un dato de tipo short con valor asignado : " + variable2);

        int variable3 = 30;
        System.out.println("Este es un dato de tipo int con valor asignado : " + variable3);

        long variable4 = 100;
        System.out.println("Este es un dato de tipo long con valor asignado : " + variable4);

        // 1.2 Decimales
        float variable5 = 5.5f;
        System.out.println("Este es un dato de float valor asignado : " + variable5);

        double variable6 = 10.5d;
        System.out.println("Este es un dato de tipo double con valor asignado : " + variable6);

        //2. Booleano
        boolean variable7 = false;
        System.out.println("Este es un dato de tipo boolean con valor asignado : " + variable7);

        boolean variable8 = true;
        System.out.println("Este es un dato de tipo boolean con valor asignado : " + variable8);

        // 3. Texto
        char variable9 = 'a';
        System.out.println("Este es un dato de tipo char con valor asignado : " + variable9);

        String variable10 = "Lorem ipsum dolor siet amet. ........";
        System.out.println("Este es un dato de tipo String con valor asignado : " + variable10);

        }
}
